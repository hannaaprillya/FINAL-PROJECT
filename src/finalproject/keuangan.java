    public ArrayList<KeuanganList> keuanganList() {
        ArrayList<KeuanganList> keuanganList = new ArrayList<>();
        try {//SELECT REPLACE(worker.isAcc,'1','Diterima') from worker
            Connection con = koneksi.getKoneksi();
            String sql = "SELECT * FROM `keuangan`";
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            KeuanganList keuangan;
            while (rs.next()) {
                keuangan = new KeuanganList(rs.getInt("id_keuangan"), rs.getInt("pemasukan"), rs.getInt("pengeluaran"));
                keuanganList.add(keuangan);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);

        }
        return keuanganList;
    }

    private void fetchData() {
        ArrayList<KeuanganList> list = keuanganList();
        DefaultTableModel model = (DefaultTableModel) dataKeuangan.getModel();
        Object[] row = new Object[4];
        for (int i = 0; i < list.size(); i++) {

            row[2] = list.get(i).getId_keuangan();
            row[1] = list.get(i).getPemasukan();
            row[0] = list.get(i).getPengeluaran();

            model.addRow(row);
        }
    }

