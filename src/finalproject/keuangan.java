    private void editTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editTxtActionPerformed
        // TODO add your handling code here:
        try {

            Connection con = koneksi.getKoneksi();
            int row = dataKeuangan.getSelectedRow();
            String value = (dataKeuangan.getModel().getValueAt(row, 0).toString());
            String sql = "UPDATE keuangan SET pengeluaran =? , pemasukan =?  WHERE id_keuangan=" + value;
            if (pemasukanTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pemasukan tidak boleh kosong");
            } else if (pengeluaranTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pengeluaran tidak boleh kosong");
            } else {
                pst = con.prepareStatement(sql);
                pst.setString(1, pemasukanTxt.getText());
                pst.setString(2, pengeluaranTxt.getText());

                pst.executeUpdate();

                DefaultTableModel model = (DefaultTableModel) dataKeuangan.getModel();
                model.setRowCount(0);
                fetchData();
                pemasukanTxt.setText("");
                pengeluaranTxt.setText("");
                JOptionPane.showMessageDialog(null, "Update Selesai");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_editTxtActionPerformed

    private void tambahBarangTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahBarangTxtActionPerformed
        // TODO add your handling code here:
        try {

            Connection con = koneksi.getKoneksi();
            //query insert data ke dalam database mysql
            pst = con.prepareStatement("INSERT INTO keuangan (pemasukan, pengeluaran) VALUES(?,?)");
            //validasi ketika textField kosong

            if (pemasukanTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pemasukan tidak boleh kosong");
            } else if (pengeluaranTxt.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Pengeluaran tidak boleh kosong");
            } else {
                //digunakan untuk memasukkan data ke masing2 variabel textfield seperti namaTxt, dll

                //misal bingung pst sama rs bisa diliat di variabel diatas
                pst.setString(1, pemasukanTxt.getText());
                pst.setString(2, pengeluaranTxt.getText());
                pst.executeUpdate();

                //setelah nginput data kasih ini biar textfieldnya kosong lagi
                pemasukanTxt.setText("");
                pengeluaranTxt.setText("");

                //setelah daftar muncul pop up dafatar berhasil dan akan tampil frame baru
                JOptionPane.showMessageDialog(null, "Pemasukan dan Pengeluaran berhasil ditambah");
                this.dispose();
                keuangan dashboard = new keuangan();
                dashboard.setVisible(true);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error " + e.getMessage());

        }
    }//GEN-LAST:event_tambahBarangTxtActionPerformed

