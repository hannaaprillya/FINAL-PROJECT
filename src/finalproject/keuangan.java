    private void hapusTxtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusTxtActionPerformed
        // TODO add your handling code here:
        try {
            int pilihan = JOptionPane.showConfirmDialog(this, " Apakah anda yakin akan menghapus data ini ?", "Konfirmasi", JOptionPane.WARNING_MESSAGE);
            if (pilihan == JOptionPane.YES_OPTION) {
                Connection con = koneksi.getKoneksi();
                int row = dataKeuangan.getSelectedRow();
                String value = (dataKeuangan.getModel().getValueAt(row, 0).toString());
                String sql = "DELETE FROM keuangan WHERE id_keuangan=" + value;
                pst = con.prepareStatement(sql);
                pst.executeUpdate();
                pemasukanTxt.setText("");
                pengeluaranTxt.setText("");
                DefaultTableModel model = (DefaultTableModel) dataKeuangan.getModel();
                model.setRowCount(0);
                fetchData();
                JOptionPane.showMessageDialog(rootPane, "Data Dihapus");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Pilih Baris Dulu");
        }
    }//GEN-LAST:event_hapusTxtActionPerformed

